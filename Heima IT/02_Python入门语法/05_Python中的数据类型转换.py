# 将数字类型转换成字符串
int_str = str(11)
print(type(int_str), int_str)

float_str = str(3.14)
print(type(float_str), float_str)

# 将字符串转换成数字
str_int = int("11")
print(type(str_int), str_int)
str_float = float("3.14")
print(type(str_float), str_float)

# 整数转浮点数
float_int = float(11)
int_float = int(3.14)
print(type(float_int), float_int)
print(type(int_float), int_float)
